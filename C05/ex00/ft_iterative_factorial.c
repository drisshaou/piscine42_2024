#include <stdio.h>

int	ft_iterative_factorial(int nb)
{
	int	nbr;

	if (nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	nbr = 1;
	while (nb >= 1)
	{
		nbr *= nb;
		nb -= 1;
	}
	return (nbr);
}
